public class GoogleMapTypes {

    String mapypes;
    String selected;
    boolean isSelected;

    public String getMapypes() {
        return mapypes;
    }

    public void setMapypes(String mapypes) {
        this.mapypes = mapypes;
    }

    public String getSelected() {
        return selected;
    }

    public void setSelected(String selected) {
        this.selected = selected;
    }

    public boolean isSelected() {
        return isSelected;
    }

    public void setSelected(boolean selected) {
        isSelected = selected;
    }
}
